#include "libft.h"

int		ft_wdlen(const char *s, int i, char c)
{
	int j;

	j = 0;
	if (!s || !c)
		return (0);
	while (s[i] != c && s[i] != '\0')
	{
		j++;
		i++;
	}
	return (j);
}
