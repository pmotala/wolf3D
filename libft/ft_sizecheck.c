#include "libft.h"

int		ft_sizecheck(int c)
{
	int		res;

	res = 0;
	if (c < 0)
		c = c * -1;
	while (c >= 0)
	{
		c = c / 10;
		res++;
		if (c == 0)
			return (res);
	}
	return (res);
}
