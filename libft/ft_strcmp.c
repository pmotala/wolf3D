#include "libft.h"

int		ft_strcmp(const char *str1, const char *str2)
{
	int		i;
	int		c;

	c = (ft_strlen(str1) + ft_strlen(str2)) / 2;
	i = 0;
	while (i <= c)
	{
		if (str1[i] == str2[i])
			i++;
		else
			return ((unsigned char)str1[i] - (unsigned char)str2[i]);
	}
	return (0);
}
